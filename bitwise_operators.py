# & , | , ^ , ~ , << , >> Operators
print(4&5)   # AND Operator
print(4|5)   # OR Operator
print(4^5)   # XOR Operator
print(~4)    # NOT Operator
print(4<<2)  # Left Shift Operator
print(4>>2)  # Right Shift Operator
print(type(4&5))
print(type(4|5))    
print(type(4^5))
print(type(~4))
print(type(4<<2))
print(type(4>>2))

